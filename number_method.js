var num=20;
console.log(typeof(num));
// Output: number
// Here, the variable num is assigned a value of 20. The typeof operator returns the data type of the variable which is a number.
num=toString(num);
console.log(typeof(num));
// Output: string
// Here, the variable num is converted to a string using the toString() method. The typeof operator returns the data type of the variable which is a string.